for (let y = 2010; y <= 2022; y++) {
    document.write(`<h1>${y}</h1>`);
    document.write('<div class="year">');
    for (let j = 0; j < 12; j++) {
        document.write('<div class="month">');
        document.write(`<h2>${j}</h2>`)
        for (let i = 1; i <= 31; i++) {
            document.write('<div class="day">');
            document.write(i);
            document.write("</div>");
        }
        document.write("</div>");
    }
    document.write("</div>");
}